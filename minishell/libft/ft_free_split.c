#include "libft.h"

void	ft_free_split(char ***split)
{
	int		i;

	if (!(*split))
		return ;
	i = -1;
	while ((*split)[++i])
		ft_strdel(&(*split)[i]);
	free(*split);
	*split = NULL;
}
